    private void carregarListaTurmas() {
        List<Turma> turmas = TelaPrincipal.turmaDao.findTurmaEntities();
        DefaultTableModel dtm = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        dtm.setColumnIdentifiers(new Object[]{"ID", "Ano", "Período", "Local", "Vagas", "Horário", "Disciplina", "Professor"});
        turmas.forEach((t) -> dtm.addRow(new Object[]{t.getId(), t.getAno(), t.getPeriodo(), t.getLocal(), t.getNumVagas(), t.getHorario(), t.getDisciplina().getNome(), t.getProfessor().getNome()}));
        this.ListaTurmas.setModel(dtm);
    }

