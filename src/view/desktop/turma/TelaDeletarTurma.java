    private void DeletarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletarButtonActionPerformed
        int row = this.ListaTurmas.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Você precisa selecionar uma linha para deletar.");
        } else {
            Long id = (Long) this.ListaTurmas.getValueAt(row, 0);
            try {
                TelaPrincipal.turmaDao.destroy(id);
                this.carregarListaTurmas();
                JOptionPane.showMessageDialog(this, "Turma removida com sucesso.");
            } catch (NonexistentEntityException ex) {
                JOptionPane.showMessageDialog(this, "Houve um erro ao tentar deletar uma turma inexistente.");
            }
        }
    }//GEN-LAST:event_DeletarButtonActionPerformed

