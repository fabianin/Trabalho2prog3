    private void carregarLista() {
        List<Professor> professores = TelaPrincipal.professorDao.findProfessorEntities();
        DefaultTableModel dtm = (DefaultTableModel) this.ListaProfessores.getModel();
        professores.forEach((p) -> {
            dtm.addRow(new Object[]{p.getId(), p.getNome(), p.getDepartamento(), p.getCpf(), p.getDisciplinasApto().size()});
        });
    }

