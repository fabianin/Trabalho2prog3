    private void DeletarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletarButtonActionPerformed
        int row = this.ListaProfessores.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Você precisa selecionar uma linha para deletar.");
        } else {
            Long id = (Long) this.ListaProfessores.getValueAt(row, 0);
            try {
                TelaPrincipal.professorDao.destroy(id);
                this.carregarListaProfessores();
                JOptionPane.showMessageDialog(this, "Professor removido com sucesso");
            } catch (NonexistentEntityException ex) {
                JOptionPane.showMessageDialog(this, "Houve um erro ao tentar deletar um professor inexistente.");
            }
        }
    }//GEN-LAST:event_DeletarButtonActionPerformed

