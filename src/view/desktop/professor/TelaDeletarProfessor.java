    private void carregarListaProfessores() {
        List<Professor> professores = TelaPrincipal.professorDao.findProfessorEntities();
        DefaultTableModel dtm = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        dtm.setColumnIdentifiers(new Object[]{"ID", "Nome", "CPF", "Departamento"});
        professores.forEach((p) -> {
            dtm.addRow(new Object[]{p.getId(), p.getNome(), p.getCpf(), p.getDepartamento()});
        });
        this.ListaProfessores.setModel(dtm);
    }

